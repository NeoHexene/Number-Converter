package com.NumCo.numberconverter.Numerals;

public class Numeral {
    private String nums;

    public Numeral(String nums) {
        this.nums = nums;
    }

    public String getValue() {
        return nums;
    }

    public String toDec() {
        return nums;
    }

    public String toHex() {
        return nums;
    }

    public String toBin() {
        return nums;
    }

    public String toOct() {
        return nums;
    }

}
